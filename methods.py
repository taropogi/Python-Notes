# methods are functions that are defined within a class and are used to perform operations using the data contained in the class's attributes.

# built in methods are predefined methods provided by Python for built-in data types like lists, strings, and dictionaries.

# for example, here are some built-in methods for lists:
my_list = [1, 2, 3, 4, 5]
my_list.append(6)  # Adds 6 to the end of the list
print(my_list)  # Output: [1, 2, 3, 4, 5, 6]

 