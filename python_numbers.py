# Arithmetic Operations in Python

# 1. Addition
result = 5 + 3
print("Addition: 5 + 3 =", result)

# 2. Subtraction
result = 10 - 4
print("Subtraction: 10 - 4 =", result)

# 3. Multiplication
result = 7 * 6
print("Multiplication: 7 * 6 =", result)

# 4. Division
result = 20 / 5
print("Division: 20 / 5 =", result)

# 5. Modulus: this will give the remainder of the division
result = 10 % 3
print("Modulus: 10 % 3 =", result)

# 6. Exponentiation: this will raise a number to the power of another number
result = 2 ** 30
print("Exponentiation: 2 ** 300 =", result)