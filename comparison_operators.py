# Comparison Operators in Python

# Equal to
a = 5
b = 5
a == b  # True

# Not equal to
a = 5
b = 3
a != b  # True

# Greater than
a = 7   
b = 5
a > b  # True

# Less than
a = 3
b = 5
a < b  # True

# Greater than or equal to
a = 5
b = 5
a >= b  # True

# Less than or equal to
a = 3
b = 5
a <= b  # True

# Example usage in conditional statements
x = 10
y = 20
if x < y:
    print("x is less than y")
else:
    print("x is not less than y")
